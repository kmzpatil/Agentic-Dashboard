"""
Tool: execute_sql_query
Executes a read-only SELECT query via the shared MCP DatabaseClient,
extracts the data, and returns a combined JSON payload containing both
the data records and the chart attributes for the downstream charting agent.
"""

import json
import re
from typing import Any, Dict, Optional

from tools._db import get_db, DEFAULT_QUERY_LIMIT, MAX_QUERY_LIMIT
from mcp_server.database import QueryValidationError


def _type_aware_fillna(df: "pd.DataFrame") -> "pd.DataFrame":
    """Fill missing values based on column dtype: numeric→0, text/date→''."""
    import pandas as pd
    for col in df.columns:
        if pd.api.types.is_numeric_dtype(df[col]):
            df[col] = df[col].fillna(0)
        else:
            df[col] = df[col].fillna("")
    return df


def execute_sql_query(
    query: str,
    chart_attributes: Optional[Dict[str, Any]] = None,
    limit: int | None = None,
) -> str:
    """
    Execute a SELECT SQL query and bundle the results with chart attributes.

    Routes through the MCP DatabaseClient which provides:
      - Query validation (blocks mutations, multi-statement)
      - Proper LIMIT enforcement
      - Clean connection management

    Args:
        query:            A valid SELECT statement.
        chart_attributes: A dictionary of chart settings generated by the
                          orchestrator (e.g., {"type": "bar", "x_axis": "month",
                          "y_axis": "revenue", "title": "Monthly Revenue"}).
        limit:            Max rows to return. Defaults to server config.

    Returns:
        A JSON string containing a dict with 'data' and 'chart_attributes' keys,
        or an error JSON payload on failure.
    """
    if chart_attributes is None:
        chart_attributes = {}

    # Strip markdown code fences the LLM sometimes wraps around the SQL.
    # NOTE: must use regex, NOT str.strip() — strip() removes individual
    # characters, which would corrupt table names (e.g. raw_videos → raw_video).
    query = query.strip()
    query = re.sub(r"^```(?:sql)?\s*\n?", "", query, flags=re.IGNORECASE)
    query = re.sub(r"\n?```\s*$", "", query).strip()

    try:
        db = get_db()
        effective_limit = min(limit or DEFAULT_QUERY_LIMIT, MAX_QUERY_LIMIT)
        df = db.run_read_only_query(query, limit=effective_limit)

        df = _type_aware_fillna(df)

        payload = {
            "data": df.to_dict(orient="records"),
            "chart_attributes": chart_attributes,
        }
        return json.dumps(payload, default=str)  # default=str handles dates/Decimals

    except QueryValidationError as exc:
        return json.dumps({"error": str(exc)})
    except Exception as exc:
        return json.dumps({"error": f"SQL Execution Error: {exc}"})

def execute_exploration_queries(queries_json: str) -> str:
    """Execute multiple lightweight exploration queries sequentially in one call.
    Expects a JSON string representing a list of SELECT SQL strings."""
    try:
        queries = json.loads(queries_json)
        if not isinstance(queries, list):
            return json.dumps({"error": "queries_json must be a JSON list of strings"})

        db = get_db()
        results = {}
        for i, q in enumerate(queries):
            q = q.strip()
            q = re.sub(r"^```(?:sql)?\s*\n?", "", q, flags=re.IGNORECASE)
            q = re.sub(r"\n?```\s*$", "", q).strip()
            if not q.lower().startswith("select"):
                results[f"query_{i+1}_error"] = "Only SELECT queries are allowed for exploration"
                continue
                
            try:
                df = db.run_read_only_query(q, limit=5)
                df = _type_aware_fillna(df)
                results[f"query_{i+1}"] = df.to_dict(orient="records")
            except Exception as e:
                results[f"query_{i+1}_error"] = str(e)

        return json.dumps(results, default=str)
    except Exception as exc:
        return json.dumps({"error": f"Failed to run exploration queries: {exc}"})